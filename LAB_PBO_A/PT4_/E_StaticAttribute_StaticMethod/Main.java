package LAB_PBO_A.PT4_.E_StaticAttribute_StaticMethod;

public class Main {
    public static void main(String[] args) {
        // Menghitung luas lingkaran dengan jari-jari 5
        double circleArea = MathUtils.calcCircleArea(5.0);

        // Menghitung luas persegi penjing dengan panjang 4 dan lebar 6
        double rectArea = MathUtils.calcCircleArea(4.0, 6.0);

        // Menampilkan hasil perhitungan luas lingkaran dan persegi panjang
        System.out.println("Circle area: " + circleArea);
        System.out.println("Rectangle area: " + rectArea);
    }
}
